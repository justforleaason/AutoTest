// function1.test.js

const { function1 } = require('./module'); // Підключаємо модуль, який ми хочемо протестувати

describe('Тест для функції 1', () => {
    it('Повинна викликати функцію 2 з правильними аргументами', () => {
        // Створюємо макет функції 2, яку функція 1 має викликати
        const mockFunction2 = jest.fn();

        // Підставляємо макет функції 2 в місце, де викликається функція 2 в функції 1
        function1(mockFunction2);

        // Перевіряємо, чи функція 2 була викликана з правильними аргументами
        expect(mockFunction2).toHaveBeenCalledWith('John', 'Doe');
    });
});
